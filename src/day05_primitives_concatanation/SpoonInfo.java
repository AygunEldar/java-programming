package day05_primitives_concatanation;

public class SpoonInfo {
    public static void main (String [] args ){
        String brand = "Karaca";
        String material = "silver";
        int price = 95;
        boolean oval = true;

        System.out.println("Brand:\t\t\t"  + brand);
        System.out.println("Material:\t\t\t" +material);
        System.out.println("Price:\t\t\t" + price);
        System.out.println("Form:\t\t\t"  +oval);
    }
}
